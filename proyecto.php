<h4>I.- Fundamentos filosóficos</h4>

    <img class="img-fluid rounded img-thumbnail" src="public/images/colegio-1.jpg" alt="" width="250" style="float: right; margin-right: 5px"><p>El Colegio José Martí es un establecimiento Particular Subvencionado de Financiamiento Compartido, dependiente de la sociedad educacional Magia Sociedad Anónima de Coquimbo. La propuesta Educativa del Colegio se desarrolla en los niveles de Pre-kinder, Kinder, primer y segundo ciclo básico (1º a 8º Año Básico).</p>
    <p>Nuestro colegio se distingue por tener una fuerte influencia y aproximación especial al ideario pedagógico, de un destacado maestro como lo fue José martí, el nació el 28 de enero de 1853, en la habana Cuba. Fue un romántico por su inspiración y rebeldía, un precursor del modernismo por sus gustos e inquietudes y por las cualidades de su prosa, un orador de gran altura, el caudillo de un pueblo al que entrego su inteligencia, su voluntad y su vida. Algunas apreciaciones de Martí, sobre el valor de la enseñanza, a continuación a mencionar:</p>
    <p><em>“Hombres vivos, hombres directos, hombres independientes, hombres amantes, eso han de formar los colegios…”</em></p>
    <p><em>“Todo esfuerzo por difundir la instrucción es vano, cuando no se acomoda la enseñanza a las necesidades, naturaleza y porvenir del que la recibe”</em></p>
    

    <p>Desde esta perspectiva quienes educamos en el colegio José Martí, estamos llamados a comprender:</p>

<p>1- Que la educación se percibe, como un espacio sociocultural, en donde desde la espiritualidad, se puede tejer la transformación social, la cual requiere de un ser humano vinculado con su realidad, activo en las decisiones y comprometido con un proyecto social, que no solo tiene alcance local sino también regional y mundial con el fin de entregar a nuestros estudiantes una visión universal, como también una mayor capacidad de comparación y conciencia social.</p>

<p>2- Que la educación es un proceso permanente el cual no tiene ni tiempo ni espacio específico, por lo cual, José Martí, no solo piensa en los niveles preescolar básica y media, como espacios formales del proceso educativo ,sino también, asigna un lugar importante a la educación no formal e informal , esto nos exige y obliga a la revisión de la perspectiva de los lazos del proceso educativo ,con la sociedad, el sujeto, la formación docente y el currículum, ya que, como también, hace mención, nuestra poetiza y maestra Gabriela Mistral :” Enseñar siempre: en el patio y en la calle como en la sala de clase. “Enseñar con la actitud, el gesto”. Podemos entonces hacer la siguiente reflexión: La palabra. Educar, no solo en la escuela como en un contexto aislado de educación, por el contrario llevarlo siempre en distintos espacios, como parte de nuestras vida,, ya que ,el ser docente, es mucho más que trasmitir conocimientos, también es ser consecuente con lo que se trasmite como pedagogo, es decir, tener conciencia espiritual de una valoración real del desempeño como profesional.</p> 

<p>3- Que la educación es un derecho de todo ciudadano, pero también es un deber de cada ciudadano, en consecuencia, el ser humano tiene responsabilidad sobre su proceso de aprendizaje en el compromiso con el, es aquí donde se entrelaza el trabajo nuestro en comunidad y con la comunidad. Por lo tanto creemos que la educación es consecuencia, de una fusión reproductora de creencias, valores, conocimientos y conciencia, que se fundamentan en redes de intercambio sociocultural, generados por vínculos afectivos, cognitivos, los cuales debemos afianzar o corregir.
En consecuencia nos proponemos formar un tipo de persona que dentro de su propia identidad, tenga la capacidad de tomar iniciativas, aplicar la reflexión y un espíritu critico constructivo frente al conocimiento y frente a la realidad social .Esto implica una pedagogía activa que se base en la participación del alumno y en la responsabilidad del profesor de mantener en el, la motivación por aprender a razonar, a dudar y examinar distintos puntos de su trabajo. Por lo cual “El maestro, debe distinguirse por su cultura, por su educación, por la pureza de sus ideales y sentimientos, su lenguaje correcto y agradable y sobre todo, por ser un ejemplo de integridad y nobleza espiritual.”</p>

<p>4- Que la educación en general se ha preocupado del rendimiento académico y disminución del fracaso escolar, sin embargo hoy nos enfrentamos a una carencia igual o mayor que esta , que es el analfabetismo emocional y social en muchos de nuestros alumnos, por lo tanto el colegio José Martí Pérez deberá ser, además un espacio donde los alumnos desarrollen actitudes y habilidades sociales necesarias para la comunicación y convivencia humana; colaboración e integración de la mayoría de sus miembros alumnos, padres y profesores por medio de la participación en las diversas actividades curriculares formales e informales.</p>

<p>5- Que el conflicto siempre estará presente, en nuestro quehacer escolar, el que por medio de una buena mediación se puede resolver. Lo que como educadores no permitiremos será la violencia escolar en cualquiera de sus manifestaciones ya sea física o sicológica, ya que no promueve la sana convivencia y el respeto por los demás. En consecuencia la comunidad martiana consideraran “mal trato o violencia sicológica” todo tipo de descalificación, burla, extorsión, discriminación, insulto, ofensa, indiferencia, gritones, sarcasmos y otros de semejante índole.</p>

<p>6- En consecuencia el grupo humano que conforma la comunidad MARTIANA, alumnos, educadores, asistentes de la educación, no docentes, padres y apoderados, consideramos pertinente y significativo para nuestra institución educacional, fundamentar nuestra practica educativa en las siguientes fuentes de principios , valores y sentimientos que a continuación manifestamos:</p>

<p>Principios que fundamentan y sustentan la opción educativa –profesional Martiana:</p>

<p>6.1- La búsqueda del bien por sobre los intereses personales.</p>
<p>6.2- El acceso a una educación de calidad concebida como el aprendizaje de habilidades destrezas y competencias cognitivas, afectivas, éticas y conductuales para el buen desarrollo de su proyecto de vida.</p>
<p>6.3- El respeto por escuchar y ser escuchado en un contexto de comunicación abierta, que tenga el fin de construir y cimentar nuestro establecimiento sobre la base del amor.</p>
<p>6.4- La valoración de la familia de una comunicación efectiva qué acentué la congruencia entre el pensar y el actuar y la asertividad en la expresión de pensamientos, intereses y sentimientos personales.</p>
<p>6.5- La valoración de la familia como el fundamento de la sociedad y una fuente poderosa de energía que promueve el desarrollo humano, mediante la entrega de un ambiente de estabilidad y apoyo incondicional en el proceso educativo y de desarrollo del individuo.</p>
<p>6.6- La valoración de la singularidad de la diferencia y de la dignidad fundamental de la persona en todas las manifestaciones y expresiones de la vida humana.</p>
<p>6.7- La valoración de que quien sabes, vale más. Saber es tener .La moneda se funde y el saber no.</p>
<p>6.8- La valoración de que la educación debe ser un proceso continuo, constante y permanente.</p>
<p>6.9- La valoración del trabajo humano, de los hábitos de trabajo y del esfuerzo como componentes principales de la construcción social de una realidad que comienza a elaborarse en la experiencia escolar.</p>

<h4>II.- Valores que orientaran y sustentaran en forma transversal la práctica educativa  de docentes y no docentes del colegio José Márti Pérez.</h4>

    <p>La comunidad educativa martiana guiara su respectivo quehacer educacional fundamentándolo en la enseñanza de los valores universales de amor, justicia y libertad, llevándolos a cabo por medio del dialogo permanente, del respeto absoluto por la diversidad y cultura, además de distinguirse Por su solidaridad y el saber escuchar. Esto se resume en un decálogo que reúne diez actitudes que resaltaran en cualquier integrante de la comunidad martianas alumnos docentes administrativos, auxiliares, paradocentes etc.)</p>

<p><strong> Decálogo Martiano.</strong></p>

<p>1-Un martiano siente que la capacidad de amar es el verdadero pergamino de nobleza de los hombres.</p>
<p>2-Un martiano cree que la dicha es de los que crean, y no de los que destruyen.</p>
<p>3- Un martiano sabe que la conciliación es la garantía de la paz.</p>
<p>4- Un martiano demuestra su cultura expresándose, cuidando su persona y su entorno.</p>
<p>5- Un martiano sabe que el amor es el sentimiento que siempre permanece joven, aun en los corazones viejos.</p>
<p>6-Un martiano sabe que la educación comienza con la vida, y no acaba sino con la muerte.</p>
<p>7-Un martiano reconoce que el destino no es cuestión de suerte, es una cuestión de elección. No es algo que se espere, es algo por alcanzar.</p>
<p>8-Un martiano sabe que para llegar a la meta se necesitan cuatros pasos: Planifica con cuidado, organizar con fe, proceder con seguridad y proseguir con constancia.</p>
<p>9-Un martiano tiene conciencia que todo acto humano se origina en una o más de estas causas: oportunidad, personalidad, impulso, habito, anhelo, pasión.</p>
<p>10-Un martiano esta consciente que no tiene importancia donde nació y cómo vivió. Es lo que ha hecho y donde haya estado lo que debiera ser de interés.</p>

<h4>III.- Visión de futuro.</h4>

    <p>El Proyecto Educativo Institucional (P.E.I.) que postula el Colegio, orienta su acción pedagógica a la formación integral de sus alumnos, logrando que a través del desarrollo y adquisición de los conocimientos, las habilidades lingüísticas, las destrezas, los valores y las actitudes estén capacitados para enfrentar los desafíos del mundo actual y de su vida futura para una adecuada inserción en nuestra sociedad.</p>

<h4>IV.-Misión.</h4>

    <p>La misión del colegio José Martí Pérez es ofrecer el más alto estándar de calidad, logrado a través del desarrollo de un proceso de Enseñanza – Aprendizaje dinámico y constructivo, con una fuerte inspiración humanista cristiana, de manera que los educandos al egresar de la Enseñanza Básica hayan logrado la preparación académica, personal y social, que le permita integrarse exitosamente a la Educación Media y proyectarse en la familia, y en la sociedad. Por lo tanto impulsaremos el constante perfeccionamiento docente en nuestros profesionales de la educación de modo que su trabajo pedagógico se realice de manera sólida y en un ambiente emocionalmente equilibrado, donde se promuevan los principios de amor, equidad, integridad y ética.</p>
